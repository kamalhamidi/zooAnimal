    return allAnimals.where((a) => a.difficulty == difficulty).toList();
  }

  static List<Animal> getByCategory(String category) {
    return allAnimals.where((a) => a.category == category).toList();
  }

  static Animal getById(String id) {
    return allAnimals.firstWhere((a) => a.id == id);
  }

  static List<Animal> getEasyAnimals() => getByDifficulty(AnimalDifficulty.easy);
  static List<Animal> getMediumAnimals() => getByDifficulty(AnimalDifficulty.medium);
  static List<Animal> getHardAnimals() => getByDifficulty(AnimalDifficulty.hard);
